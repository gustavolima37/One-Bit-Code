# Listas

# Lista com valores diversos
gameFifa = ["Fifa 23", 2022, 90.50, True]
print(gameFifa)

# Lista com valores unicos - Ex: strings
gameList = ["Resident Evil 4", "Star Wars Jedi Survivor", 
            "The Legend of Zelda", "Red Dead 2", "Mario Odyssey"]
print(gameList)

# 1 - Buscar os dois primeiros itens da lista
print(gameList[:2])

# 2 - Buscar o último item da lista
print(gameList[-1])

# 3 - Buscar jogos até uma determinada posição
print(gameList[:3])

# 4 - Buscar jogos de uma posição em diante
print(gameList[1:4])